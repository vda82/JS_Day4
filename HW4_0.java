package day4.HWforwhileetc;

public class HW4_0 {

	public static void main(String[] args) {
		int[] x = new int [28];
		
		for(int i = 0; i<x.length; i++) {
			x[i] = i*365;
			
		System.out.println("Zero element is " + x[0]);
		System.out.println("14th element is " + x[14]);
		System.out.println("27th element is " + x[27]);
		}

	}

}
